public class PostalCode {
    private String tk;
    private String dhmos;
    private String perifereia;

    public String getTk() {
        return tk;
    }

    public void setTk(String tk) {
        this.tk = tk;
    }

    public String getDhmos() {
        return dhmos;
    }

    public void setDhmos(String dhmos) {
        this.dhmos = dhmos;
    }

    public String getPerifereia() {
        return perifereia;
    }

    public void setPerifereia(String perifereia) {
        this.perifereia = perifereia;
    }
}
